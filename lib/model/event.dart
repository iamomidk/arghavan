class Event {
  String start;
  String end;
  String location;
  String title;
  List<String> requirements;
  String time;
  String requester;

  Event({
    required this.start,
    required this.end,
    required this.location,
    required this.title,
    required this.requirements,
    required this.time,
    required this.requester,
  });
}
